#!/bin/bash

cat *.txt | \
    sed -e '/^---$/,/^...$/d' \
        -e '/!\?\[.*\]\((.*)\|:.*\)/d' | \
    sed -e 's/["_~.!?,():;<>{}#|*]//g' \
        -e 's/\]\[/ /g' \
        -e 's/-/ /g' | \
    sed -e 's/ /\n/g' | \
    sed -e '/^$/d' \
        -e '/^\\$/d' | \
    sed -e 's/\[\?\(.*\)\]\[.*\]/\1/g' \
        -e 's/\]\[\]//g' \
        -e 's/^\[//' | \
    sort | \
    uniq | \
    sort

# [feline.sh][]
